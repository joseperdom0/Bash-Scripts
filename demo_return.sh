#!/bin/bash
function given_args {
        if [ $# -lt 1 ]
        then
        echo -e "Number of arguments: $#"
        return 1
        else
        echo -e "Number of arguments: $#"
        return 0
        fi
}


# No arguments given
given_args
echo -e "Function status code: $?\n"

# One argument given
given_args "argument"
echo -e "Function status code: $?\n"
# Pass the results of the funtion into a variable
content=$(given_args "argument")
echo -e "Content of the variable: \n\t$content"
